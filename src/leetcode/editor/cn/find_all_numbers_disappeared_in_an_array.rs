impl Solution {
    pub fn find_disappeared_numbers(mut nums: Vec<i32>) -> Vec<i32> {
        for i in 0..nums.len() {
            let next_index = (nums[i].abs() - 1) as usize;
            nums[next_index] = -nums[next_index].abs();
        }
        nums.into_iter()
            .enumerate()
            .filter(|(_, n)| n.is_positive())
            .map(|(i, _)| (i + 1) as i32)
            .collect()
    }
}
